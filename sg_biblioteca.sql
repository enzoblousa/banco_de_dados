use gerenciamentoBiblioteca;

create table livros(
	id int primary key,
    titulo varchar(100),
    autor_id int,
    ano_publicacao int,
    genero varchar(30),
    descricao text,
    disponibilidade boolean,
	FOREIGN KEY (autor_id) REFERENCES autores(id)
);
ALTER TABLE livros modify ano_publicacao int;
create table autores(id int primary key, nome varchar(100), nacionalidade char(2), data_nascimento date);

create table usuarios(id int primary key, nome varchar(100), email varchar(100), telefone varchar(20), data_nascimento date);

create table emprestimos( id int primary key, id_usuario int, id_livro int, data_emprestimo datetime, data_devolucao_prevista datetime, data_devolucao datetime, status int, FOREIGN KEY (id_usuario) REFERENCES usuarios(id),
    FOREIGN KEY (id_livro) REFERENCES livros(id)); 
    
create table reservas(id int primary key, id_usuario int, id_livro int, data_reserva datetime, status_reserva int, FOREIGN KEY (id_usuario) REFERENCES usuarios(id),
    FOREIGN KEY (id_livro) REFERENCES livros(id));


